# ۳. ساخت دیکشنری رشته‌ها
major_dict = {m['name']: m for m in all_majors}

# ۴. محاسبهٔ هم‌پوشانی جاکارد
def jaccard(set1, set2):
    if not set1 or not set2: return 0
    return len(set1 & set2) / len(set1 | set2)

# ۵. خوشه‌بندی ساده برای هر زیر-قلمرو
narrow_paths = {}
for realm, sub_dict in sub_realms.items():
    narrow_paths[realm] = {}
    for sub_name, major_names in sub_dict.items():
        major_codes = {name: set(major_dict[name].get('micro_motive_codes', []))
                       for name in major_names if name in major_dict}
        if len(major_codes) <= 3:
            narrow_paths[realm][sub_name] = [{
                "path_name": f"{sub_name}",
                "majors": list(major_codes.keys())
            }]
            continue

        clusters, used = [], set()
        names = list(major_codes.keys())
        for name in names:
            if name in used: continue
            cluster = [name]; used.add(name)
            for other in names:
                if other not in used and jaccard(major_codes[name], major_codes[other]) >= 0.15:
                    cluster.append(other); used.add(other)
            clusters.append(cluster)

        narrow_paths[realm][sub_name] = [
            {"path_name": f"{sub_name} - مسیر {i+1}", "majors": c}
            for i, c in enumerate(clusters)
        ]

# ۶. ذخیره
out = os.path.join(DATA_DIR, 'narrow_paths.json')
with open(out, 'w', encoding='utf-8') as f:
    json.dump(narrow_paths, f, ensure_ascii=False, indent=2)
print(f"✅ مسیرهای باریک در '{out}' ذخیره شد.")
